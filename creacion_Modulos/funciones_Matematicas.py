#Creamos lkos modulos basicos, los cuales son:
# SUMA, RESTA, MULTIPLICACION Y DIVICION
# 
def Sumar(valor1, valor2):
    suma = valor1+valor2
    print("El resultado de la SUMA es: ",suma)

def Restar(valor1, valor2):
    resta = valor1-valor2
    print("El resultado de la Resta es: ", resta)

def Multiplicar(valor1, valor2):
    multiplica = valor1*valor2
    print("El resultado de la multiplica es: ",multiplica)

def Dividir(valor1, valor2):
    divide = valor1/valor2
    print("El resultado de la divide es: ",divide)

